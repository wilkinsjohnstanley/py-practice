# tuples
tuple_example = (1, 2, 3, 4, 5)
print(tuple_example[0])  # Output: 1
print(tuple_example[1:4])  # Output: (2, 3, 4)
# Tuples are immutable, so you cannot change their values   
a, b, c, d, e = tuple_example  # Unpacking the tuple into variables
print(a)  # Output: 1   
print(b)  # Output: 2
print(c)  # Output: 3
print(d)  # Output: 4
print(e)  # Output: 5
